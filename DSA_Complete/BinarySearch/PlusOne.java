package BinarySearch;

public class PlusOne {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3};
        String s = "abc";  // Declare String properly

        // Loop through the array and print the concatenated value
        for (int ele : arr) {
            System.out.println(s + ele); // Concatenate String with integer element
        }
    }
}

